    private void updateTable(List<CustomerDTO> customers) {
        // Clear the existing data
        
        model.setRowCount(0);
        // Populate the table model
        for (CustomerDTO customer : customers) {     
            Object[] row = new Object[]{customer.getId(), customer.getLastname(), customer.getName(), customer.getSdt(), customer.getGender()};
            model.addRow(row);   
        }
    }
