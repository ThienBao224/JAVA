    public void showTable(){
        model.setRowCount(0);
        ArrayList<CustomerDTO> arr = khBUS.getCustomer();
        for (CustomerDTO customerDTO : arr) {
            Object[] row = {customerDTO.getId(),customerDTO.getLastname(),customerDTO.getName(),customerDTO.getGender(),customerDTO.getSdt()};
            model.addRow(row);
        }
        tbCustomer.addMouseListener(new MouseAdapter(){
                @Override
            public void mouseClicked(MouseEvent e) {
        int selectedRow = tbCustomer.getSelectedRow();
         int id = (int) tbCustomer.getValueAt(selectedRow, 0);
        String lastname = (String) tbCustomer.getValueAt(selectedRow, 1);
        String name = (String) tbCustomer.getValueAt(selectedRow, 2);
        String gender = (String) tbCustomer.getValueAt(selectedRow, 3);
        int phone = (int)tbCustomer.getValueAt(selectedRow, 4);
        lbMaKH.setText(String.valueOf(id));
        textfieldLastName.setText(lastname);
        TextFieldName.setText(name);
        jComboBoxGioitinh.setSelectedItem(gender);
        TextFieldPhone.setText(String.valueOf(phone));
    }
        });
    }
