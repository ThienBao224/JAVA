    private void jButtonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchActionPerformed
        // TODO add your handling code here:
        ArrayList<ManufactureDTO> arr = nccBUS.Find(Integer.parseInt(jTextFieldSearch.getText()), jTextFieldSearch.getText());
        ArrayList<ManufactureDTO> dstk = new ArrayList<>();
        String searchText = jTextFieldSearch.getText().trim();

        // Kiểm tra xem searchText có phải là số hay không
        boolean isNumeric = searchText.matches("\\d+"); // Sử dụng biểu thức chính quy để kiểm tra

        for (ManufactureDTO manufactureDTO : arr) {
            if (isNumeric) {
                // Tìm kiếm theo mã NCC
                if (manufactureDTO.getMaNCC() == Integer.parseInt(searchText)) {
                    dstk.add(manufactureDTO);
                }
            } else {
                // Tìm kiếm theo tên NCC (không phân biệt hoa thường)
                if (manufactureDTO.getTenNCC().equalsIgnoreCase(searchText)) {
                    dstk.add(manufactureDTO);
                }
            }
        }

        model.setRowCount(0);
        for (ManufactureDTO manufactureDTO : dstk) {
            model.addRow(new Object[]{manufactureDTO.getMaNCC(), manufactureDTO.getTenNCC(), manufactureDTO.getSdt(), manufactureDTO.getDiaChi()});
        }

        if (dstk.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Không tìm thấy");
        }

    }//GEN-LAST:event_jButtonSearchActionPerformed

