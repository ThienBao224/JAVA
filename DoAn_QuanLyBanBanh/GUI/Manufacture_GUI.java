    public void showTable(){
        model = new DefaultTableModel();
        model.addColumn("Mã nhà cung cấp");
        model.addColumn("Tên nhà cung cấp");
        model.addColumn("Số điện thoại");
        model.addColumn("Địa chỉ");
        jTableManufacture.setModel(model);
        ArrayList<ManufactureDTO> arr = nccBUS.getManufacture();
        for (ManufactureDTO manufactureDTO : arr) {
          Object[] row = {manufactureDTO.getMaNCC(),manufactureDTO.getTenNCC(),manufactureDTO.getSdt(),manufactureDTO.getDiaChi()};  
          model.addRow(row);
        }
        jTableManufacture.addMouseListener(new MouseAdapter(){
             @Override
             public void mouseClicked(MouseEvent e) {
        // Lấy chỉ mục của dòng được chọn
        int selectedRow = jTableManufacture.getSelectedRow();

        // Truy xuất dữ liệu từ dòng được chọn
        int id = (int) jTableManufacture.getValueAt(selectedRow, 0);
        String name = (String) jTableManufacture.getValueAt(selectedRow, 1);
        String address = (String) jTableManufacture.getValueAt(selectedRow, 3);
        int phone = (int) jTableManufacture.getValueAt(selectedRow, 2);
        jLabelMaNCC.setText(String.valueOf(id));
        jTextFieldTenNCC.setText(name);
        jTextFieldSDT.setText(String.valueOf(phone));
        jTextFieldDiaChi.setText(address);
             }
        });
        
    }
